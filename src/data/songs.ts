export const getRandomSongPair = (): [Song, Song] => {
  const availableSongs = [...songs];
  const firstIndex = Math.floor(Math.random() * availableSongs.length);
  const firstSong = availableSongs[firstIndex];
  availableSongs.splice(firstIndex, 1);
  
  const secondIndex = Math.floor(Math.random() * availableSongs.length);
  const secondSong = availableSongs[secondIndex];
  
  return [firstSong, secondSong];
}; 